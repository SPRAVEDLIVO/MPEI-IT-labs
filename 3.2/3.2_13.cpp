#include <iostream>
#include <math.h>
#define rows 10
#define columns 2
using namespace std;

int main() {
	double A[rows][columns], diff;
	bool flag = true;
	for (int i = 0; i < rows; i++) {
		for (int j = 0; j < columns; j++) {
			cout << "A[" << i << "][" << j << "] = "; cin >> A[i][j];
		}
	}
	for (int i = 0; i < rows; i++) {
		diff = fabs(A[i][0] - A[i][1]);
		if (diff > 0.01) {
			flag = false;
			break;
		}
		cout << diff << endl;
	}
	if (flag) {
		cout << "Considered the entire two dimensional array." << endl;
	}
	system("pause");
	return 0;

}

